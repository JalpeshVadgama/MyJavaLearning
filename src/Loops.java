public class Loops {

    public static void main(String[] args){

        //While loop;
        System.out.println("While Loop");
        int value =0;
        while(value<10){
            System.out.println(value);
            value = value +1;
        }

        System.out.println("For loop");
        //For loop
        for (int i = 0; i <10 ; i++) {
            System.out.println(i);
        }


    }
}
